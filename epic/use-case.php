	<h2>Use Case</h2>
	<p>Precondition: Logged in as a registered user</p>
	<p>Postcondition: Can view map of rides</p>
	<h3>Interaction Flow</h3>
	<ul>
		<li>Click on 'Upload Ride'</li>
		<li>A select file dialog comes up</li>
		<li>Select GPX file from computer</li>
		<li>Click 'Upload'</li>
		<li>User has an option to enter details</li>
		<li>Enter a title for the ride</li>
		<li>Enter comments about the ride</li>
		<li>Select 'Save'</li>
		<li>Click 'View My Rides'</li>
		<li>User gets a confirmation of ride save</li>
		<li>Automatically loads a map of previously uploaded rides including the recent addition</li>
	</ul>

